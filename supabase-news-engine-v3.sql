-- 已有 admin_users 表时，管理员可读取；自动化使用 service_role 写入并绕过 RLS。
drop policy if exists "admins read news sources" on public.news_sources;
create policy "admins read news sources" on public.news_sources for select to authenticated using (
  exists(select 1 from public.admin_users a where a.user_id=auth.uid() and a.is_active=true)
);
drop policy if exists "admins manage news sources" on public.news_sources;
create policy "admins manage news sources" on public.news_sources for all to authenticated using (
  exists(select 1 from public.admin_users a where a.user_id=auth.uid() and a.is_active=true)
) with check (
  exists(select 1 from public.admin_users a where a.user_id=auth.uid() and a.is_active=true)
);
drop policy if exists "admins read automation logs" on public.automation_logs;
create policy "admins read automation logs" on public.automation_logs for select to authenticated using (
  exists(select 1 from public.admin_users a where a.user_id=auth.uid() and a.is_active=true)
);
drop policy if exists "admins read article versions" on public.article_versions;
