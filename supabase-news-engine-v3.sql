create table if not exists public.article_versions (
  id bigint generated by default as identity primary key,
  article_id uuid not null references public.articles(id) on delete cascade,
  snapshot jsonb not null,
  changed_by uuid,
  created_at timestamptz default now()
);

create or replace function public.capture_article_version()
returns trigger language plpgsql security definer as $$
begin
  if tg_op = 'UPDATE' then
    insert into public.article_versions(article_id, snapshot, changed_by)
    values (old.id, to_jsonb(old), auth.uid());
    new.updated_at = now();
  end if;
  return new;
end;
$$;

drop trigger if exists trg_capture_article_version on public.articles;
create trigger trg_capture_article_version before update on public.articles
for each row execute function public.capture_article_version();

